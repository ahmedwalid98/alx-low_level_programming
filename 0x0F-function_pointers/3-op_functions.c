#include "3-calc.h"
/**
 * op_add - sums two numbers
 * @a: first number
 * @b: second number
 * Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subs two numbers
 * @a: first number
 * @b: second number
 * Return: the sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - muls two numbers
 * @a: first number
 * @b: second number
 * Return: the mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides two numbers
 * @a: first number
 * @b: second number
 * Return: the divide
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mods two numbers
 * @a: first number
 * @b: second number
 * Return: the mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}
